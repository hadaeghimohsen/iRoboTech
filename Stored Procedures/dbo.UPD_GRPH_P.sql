CREATE PROCEDURE [dbo].[UPD_GRPH_P]
	-- Add the parameters for the stored procedure here
	@Ghid BIGINT,
	@Grph_Desc NVARCHAR(250),
	@Robo_Rbid BIGINT
AS
BEGIN
 	-- بررسی دسترسی کاربر
	DECLARE @AP BIT
	       ,@AccessString VARCHAR(250);
	SET @AccessString = N'<AP><UserName>' + SUSER_NAME() + '</UserName><Privilege>75</Privilege><Sub_Sys>12</Sub_Sys></AP>';	
   EXEC iProject.dbo.SP_EXECUTESQL N'SELECT @ap = DataGuard.AccessPrivilege(@P1)',N'@P1 ntext, @ap BIT OUTPUT',@AccessString , @ap = @ap output
   IF @AP = 0 
   BEGIN
      RAISERROR ( N'خطا - عدم دسترسی به ردیف 75 سطوح امینتی', -- Message text.
               16, -- Severity.
               1 -- State.
               );
      RETURN;
   END
   -- پایان دسترسی
   UPDATE dbo.Group_Header
      SET GRPH_DESC = @Grph_Desc,
          ROBO_RBID = @Robo_Rbid
    WHERE GHID = @Ghid;
END
GO
