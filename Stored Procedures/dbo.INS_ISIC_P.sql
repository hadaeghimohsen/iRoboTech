CREATE PROCEDURE [dbo].[INS_ISIC_P]
	-- Add the parameters for the stored procedure here
	@Isic_Desc NVARCHAR(250),
	@Stat VARCHAR(3)
AS
BEGIN
 	-- بررسی دسترسی کاربر
	DECLARE @AP BIT
	       ,@AccessString VARCHAR(250);
	SET @AccessString = N'<AP><UserName>' + SUSER_NAME() + '</UserName><Privilege>13</Privilege><Sub_Sys>12</Sub_Sys></AP>';	
   EXEC iProject.dbo.SP_EXECUTESQL N'SELECT @ap = DataGuard.AccessPrivilege(@P1)',N'@P1 ntext, @ap BIT OUTPUT',@AccessString , @ap = @ap output
   IF @AP = 0 
   BEGIN
      RAISERROR ( N'خطا - عدم دسترسی به ردیف 13 سطوح امینتی', -- Message text.
               16, -- Severity.
               1 -- State.
               );
      RETURN;
   END
   -- پایان دسترسی
   INSERT INTO dbo.Isic_Category
           ( ISIC_DESC, STAT )
   VALUES  ( @Isic_Desc, -- ISIC_DESC - nvarchar(250)
             @Stat -- STAT - varchar(3)
             )
END
GO
